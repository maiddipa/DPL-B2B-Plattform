	,[ICDurchfuehrenAm]
	,[ZuAvisieren]
	,[IstAvisiert]
	,[AvisiertVon]
	,[AvisiertAm]
	,[IstAusAblehnung]
	,[IcGrundID]
	,[OrderManagementID]
	--,[RowGuid]
	--,[VersionCol]
	,[StackHeightMin]
	,[StackHeightMax]
	,[SupportsRearLoading]
	,[SupportsSideLoading]
	,[SupportsJumboVehicles]
	,[SupportsIndividual]
	,[BasePalletTypeId]
	,[BaseQualityId]
	,[ShowOnline]
	,[IsCreatedOnline]
	,1 AS [SupportsPartialMatching]
	,[IsHotspotOffer]
	,[HotspotComment]
	,CONVERT(VARCHAR(33), [HotspotOfferUntil], 126) AS [HotspotOfferUntil]
	,[OnlineComment]
	,[LiveDocumentNumber]
	,[ExpressCode]
	,[HasBasePallets]
	,[LoadingLocationId]
FROM [LMS].[dbo].[LMS_AVAILABILITY]
WHERE CONVERT(DATE, CreationDate) = '03.06.2020'
	AND CreatedBy = 'schmidt'
	AND Quantity > 1
ORDER BY [AvailabilityId] DESC
